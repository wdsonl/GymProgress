package com.example.gymprogress.db;

public class exercises {

    private int id;
    private String exercise_name;
    private String exercise_weight;
    private String exercise_category;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getexercise_name() {
        return exercise_name;
    }

    public void setexercise_name(String exercise_name) {
        this.exercise_name = exercise_name;
    }

    public String getexercise_weight() {
        return exercise_weight;
    }

    public void setexercise_weight(String exercise_weight) {
        this.exercise_weight = exercise_weight;
    }

    public String getexercise_category() {
        return exercise_category;
    }

    public void setexercise_category(String exercise_category) {
        this.exercise_category = exercise_category;
    }
}
